    private void cliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cliActionPerformed
       if(cli.getSelectedIndex() < 0)
            JOptionPane.showMessageDialog(null, "Selecionar um cliente");
        else{
            String[] res = cli.getSelectedItem().toString().split(" - ");
            Cliente cli = con.consultarClienteCodigo(Integer.parseInt(res[0]));
            nome_cliente.setText(cli.getNome_cliente());
            cpf.setText(cli.getCpf());
            cadastro.setText(cli.getCadastro());
            rg.setText(cli.getRg()+"");
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            String dataFormatada = cli.getData_nasc().format(formatter);
            data_nasc.setText(dataFormatada);
            int ns = 0;
            if ((cli.getSexo()+"").equalsIgnoreCase("M") || (cli.getSexo()+"").equalsIgnoreCase("F"))
                ns = 1;
            sexo.setSelectedIndex(ns);
            }
    }//GEN-LAST:event_cliActionPerformed

