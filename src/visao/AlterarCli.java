    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Cliente cli2 = new Cliente();
        cli2.setNome_cliente(nome_cliente.getText());
        cli2.setCpf(cpf.getText());
        cli2.setRg(rg.getText());
        cli2.setCadastro(cadastro.getText());
        cli2.setSexo((sexo.getSelectedIndex() == 0) ? 'M' : 'F');

   
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNasc = LocalDate.parse(data_nasc.getText(), formatter);
        cli2.setData_nasc(dataNasc);


        String[] res = cli.getSelectedItem().toString().split(" - ");
        cli2.setCod_cliente(Integer.parseInt(res[0]));

        ClienteControle ins = new ClienteControle();
        String res1 = ins.alterarCliente(cli2);
        JOptionPane.showMessageDialog(null, res1);

       
        nome_cliente.setText("");
        cpf.setText("");
        rg.setText("");
        cadastro.setText("");
        sexo.setSelectedIndex(0);
        data_nasc.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

